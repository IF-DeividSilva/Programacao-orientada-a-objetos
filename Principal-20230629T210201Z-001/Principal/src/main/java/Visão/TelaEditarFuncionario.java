    private void cbFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbFuncionarioActionPerformed
        String id = String.valueOf(cbFuncionario.getSelectedItem());
        int z = Integer.parseInt(id) - 1;
        txtNome.setText(Principal.listaDados.get(z).getNome());
        txtSalario.setText(String.valueOf(Principal.listaDados.get(z).getSalario()));
        cbCargo.setSelectedItem(Principal.listaDados.get(z).getCargo()); 
    }//GEN-LAST:event_cbFuncionarioActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        int n = Principal.listaDados.size();
        if(n <= 0){
           JOptionPane.showMessageDialog(null, "Não há registros para serem alterados!","Aviso",JOptionPane.WARNING_MESSAGE);
           TelaRH tr = new TelaRH();
            setVisible(false);
            tr.setVisible(true);
            limparCampos();
        }else{
        
        String numero;
        for (int i=1; i<=n; i++) {
            numero = String.valueOf(i);
            cbFuncionario.addItem(numero);
        }       
            String id = String.valueOf(cbFuncionario.getSelectedItem());
            int z = Integer.parseInt(id) - 1;
            txtNome.setText(Principal.listaDados.get(z).getNome());
            txtSalario.setText(String.valueOf(Principal.listaDados.get(z).getSalario()));
            cbCargo.setSelectedItem(Principal.listaDados.get(z).getCargo());
        }
    }//GEN-LAST:event_formWindowOpened

