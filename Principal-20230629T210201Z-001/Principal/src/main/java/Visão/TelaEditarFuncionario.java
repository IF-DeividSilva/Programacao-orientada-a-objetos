    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        try{
            int n = Principal.listaDados.size();
            String id = String.valueOf(cbFuncionario.getSelectedItem());
            int z = Integer.parseInt(id) - 1;
            Principal.listaDados.get(z).setNome(txtNome.getText());
            Principal.listaDados.get(z).setSalario(Float.parseFloat(txtSalario.getText()));
            Principal.listaDados.get(z).setCargo(String.valueOf(cbCargo.getSelectedItem()));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, ""+e.getMessage(),"Erro",JOptionPane.ERROR_MESSAGE);
        }
        JOptionPane.showMessageDialog(null,"Informações alteradas com sucesso!","Aviso",JOptionPane.INFORMATION_MESSAGE);
        TelaRH tr = new TelaRH();
        setVisible(false);
        tr.setVisible(true);
        limparCampos();
    }//GEN-LAST:event_btEditarActionPerformed

    private void btCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCancelarActionPerformed
        TelaRH tr = TelaRH.iniciar();
        setVisible(false);
        tr.setVisible(true);
        limparCampos();
    }//GEN-LAST:event_btCancelarActionPerformed

    /**
     * @param args the command line arguments
     */
    public void limparCampos(){
        txtNome.setText("");
        txtSalario.setText("");
        cbCargo.setSelectedIndex(-1);
    }
    
